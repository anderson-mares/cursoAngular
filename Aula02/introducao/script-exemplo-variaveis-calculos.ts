//Declarando nossa variavel
let x : number = 3.14;
let y : number = 2;

//Realizando os cálculos
let soma = x + y; //Soma x + y

let subt = x - y; //Substrai x - y

let divs = x / y; //Divide x por y

let mult = x * y; //Multiplica x por y

let expo = x ** y //Eleva x à y

let mod = x % y; //Captura o resto. Exemplo: 5 dividido em 2,
                 //Sobra 1, logo meu resto de divisão será 1

//Impimindo o valor de nossa variável
console.log("Soma: " + soma);
console.log("Subtração: " + subt);
console.log("Divisão: " + divs);
console.log("Multiplicação: " + mult);
console.log("Potenciação: " + expo);
console.log("Resto de divisão: " + mod);